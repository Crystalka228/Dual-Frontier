using System;

namespace DualFrontier.Contracts.Modding;

/// <summary>
/// Точка входа мода. Каждая сборка мода содержит ровно один класс,
/// реализующий этот интерфейс. ModLoader находит реализацию по рефлексии
/// и вызывает <see cref="Initialize"/> на старте и <see cref="Unload"/>
/// при выгрузке (горячая перезагрузка или закрытие игры).
/// </summary>
public interface IMod
{
    /// <summary>
    /// TODO: Фаза 2 — вызывается один раз при загрузке мода.
    /// Здесь мод регистрирует свои компоненты/системы через <paramref name="api"/>
    /// и подписывается на интересующие события. Блокирующие операции запрещены —
    /// метод должен отработать быстро, загрузка других модов ждёт.
    /// </summary>
    void Initialize(IModApi api);

    /// <summary>
    /// TODO: Фаза 2 — вызывается при выгрузке мода. Обязан отписаться от всех
    /// событий и освободить ресурсы. После возврата мод выгружается из своего
    /// AssemblyLoadContext и не должен удерживать ссылки на объекты ядра.
    /// </summary>
    void Unload();
}
