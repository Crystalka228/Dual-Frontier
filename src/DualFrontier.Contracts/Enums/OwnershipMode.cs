namespace DualFrontier.Contracts.Enums;

/// <summary>
/// Режим владения големом со стороны мага-хозяина.
/// Определяет поведение системы привязки (<c>GolemBondComponent</c>) при
/// смене хозяина, перехвате управления и разрыве связи.
/// Используется одновременно компонентами (Components.Magic) и событиями
/// (Events.Magic), поэтому живёт в Contracts.
/// </summary>
public enum OwnershipMode
{
    /// <summary>
    /// Стандартный режим: голем прочно привязан к одному магу.
    /// Полный контроль, штатное содержание через ману.
    /// </summary>
    Bonded,

    /// <summary>
    /// Связь оспаривается: другой маг пытается перехватить управление.
    /// Счётчик <c>TicksSinceContested</c> растёт, по таймауту — смена хозяина.
    /// </summary>
    Contested,

    /// <summary>
    /// Голем покинут: прежний хозяин мёртв/исчез, нового ещё нет.
    /// Голем неактивен, но сущность не удаляется — можно перепривязать.
    /// </summary>
    Abandoned,

    /// <summary>
    /// Владение только что передано новому магу (переходное состояние одного тика).
    /// На следующем тике переходит в <see cref="Bonded"/>.
    /// </summary>
    Transferred,
}
